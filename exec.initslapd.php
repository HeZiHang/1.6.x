	$f[]="DESC=\"enhanced syslogd\"";
	$f[]="NAME=rsyslog";
	$f[]="";
	$f[]="RSYSLOGD=rsyslogd";
	$f[]="RSYSLOGD_BIN=/usr/sbin/rsyslogd";
	$f[]="RSYSLOGD_OPTIONS=\"-c4\"";
